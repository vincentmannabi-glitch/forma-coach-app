export function getSnackById(id) {
  return SNACKS.find((s) => s.id === id) || null
}

export function searchSnacksByText(q) {
  const qq = (q || '').trim().toLowerCase()
  if (!qq) return SNACKS.slice(0, 20)
  return SNACKS.filter(
    (s) =>
      s.name.toLowerCase().includes(qq) ||
      s.ingredientBlob.toLowerCase().includes(qq) ||
      s.tags.some((t) => t.includes(qq)),
  ).slice(0, 30)
}
