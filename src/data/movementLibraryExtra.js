/**
 * Extended movement library entries beyond exercises.js — used to reach 166 total with chat.
 * Each entry is generated with evidence-informed defaults; unique names get richer search.
 */

function guessPattern(name) {
  const n = name.toLowerCase()
  if (/squat|lunge|split|leg press|step-up|wall sit/.test(n)) return 'squat / knee-dominant'
  if (/deadlift|rdl|hinge|good morning|hip thrust|bridge|swing|pull-through/.test(n)) return 'hinge'
  if (/pulldown|pull-up|chin|lat pull|straight-arm pulldown/.test(n)) return 'pull (vertical)'
  if (/row|pullover|face pull|reverse fly|curl|extension|pushdown|kickback|shrug/.test(n))
    return /curl|extension|pushdown|kickback/.test(n) ? 'isolation / accessory' : 'pull (horizontal)'
  if (/press|bench|push-up|dip|fly|raise|pushdown/.test(n)) return /vertical|overhead|ohp|shoulder press/.test(n) ? 'push (vertical)' : 'push (horizontal)'
  if (/plank|carry|walk|pallof|dead bug|bird|crawl|brace/.test(n)) return 'brace / core / carry'
  if (/run|bike|rower|ski|assault|sprint|jump|bound/.test(n)) return 'conditioning / locomotion'
  return 'compound / general strength'
}

function equipmentGuess(name) {
  const n = name.toLowerCase()
  if (/cable/.test(n)) return ['cable stack']
  if (/dumbbell|db |dumbell/.test(n)) return ['dumbbells']
  if (/barbell|bb /.test(n)) return ['barbell', 'plates']
  if (/machine|leg curl|leg extension|pec deck/.test(n)) return ['machine']
  if (/kettlebell|kb /.test(n)) return ['kettlebell']
  if (/band|resistance tube/.test(n)) return ['resistance band']
  if (/bodyweight|push-up|pull-up|plank|lunge|jump/.test(n)) return ['bodyweight', 'optional mat']
  return ['varies — see movement setup']
}

function buildTemplateEntry(name) {
  const pattern = guessPattern(name)
  const eq = equipmentGuess(name)
  return {
    id: `lib-${name.toLowerCase().replace(/[^a-z0-9]+/g, '-').slice(0, 48)}`,
    name,
    aliases: [name.toLowerCase()],
    movementPattern: pattern,
    equipment: eq,
    musclesPrimary: 'Varies by execution; typically targets the prime movers for this pattern (see pattern).',
    musclesSecondary: 'Synergists and helpers for the same pattern — often smaller muscles assisting the prime movers.',
    stabilizers: 'Core, hips, scapular stabilizers, and ankles/feet often work isometrically to keep joints stacked.',
    howTo: `Set up equipment safely. Brace your midsection. Move only through a range you control — no sharp joint pain. For "${name}", follow the same pattern principles as other ${pattern} movements: joint stacking first, then load, then reps. If you are new, use lighter load and film yourself or ask a coach for a form check.`,
    mistakes: `Rushing reps, losing brace, shrugging instead of using the target muscles, cutting range short, or loading before you own the pattern.`,
    easier: `Less load, shorter range, more supported position (seated, machine, or hands-assisted), or fewer sets.`,
    harder: `More load, slower tempo on the lowering phase, pauses, or a harder variation of the same pattern.`,
    feelsLike: `Muscles targeted by the pattern should do most of the work — not joints grinding or low back taking over.`,
    whyProgram: `Movements like this fill gaps in push/pull/hinge/squat balance so you stay strong and resilient, not lopsided.`,
  }
}

